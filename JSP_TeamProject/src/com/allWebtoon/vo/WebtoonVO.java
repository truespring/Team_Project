package com.allWebtoon.vo;

public class WebtoonVO {
	private int w_no;
	private String w_thumbnail;
	private String w_title;
	private String w_story;
	private String w_link;
	private int w_platform;
	
	
	public int getW_no() {
		return w_no;
	}
	public void setW_no(int w_no) {
		this.w_no = w_no;
	}
	public String getW_thumbnail() {
		return w_thumbnail;
	}
	public void setW_thumbnail(String w_thumbnail) {
		this.w_thumbnail = w_thumbnail;
	}
	public String getW_title() {
		return w_title;
	}
	public void setW_title(String w_title) {
		this.w_title = w_title;
	}
	public String getW_story() {
		return w_story;
	}
	public void setW_story(String w_story) {
		this.w_story = w_story;
	}
	public String getW_link() {
		return w_link;
	}
	public void setW_link(String w_link) {
		this.w_link = w_link;
	}
	public int getW_platform() {
		return w_platform;
	}
	public void setW_platform(int w_platform) {
		this.w_platform = w_platform;
	}
	
}